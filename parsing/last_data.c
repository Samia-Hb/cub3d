#include "../cub3d.h"

void	map_updating(char ***map, int height)
{
	int		i;
	int		j;
	char	c;

	i = 0;
	c = 0;
	while ((*map)[i])
	{
		j = 0;
		if (i == 0 || i == height - 1)
			c = '1';
		else
			c = '0';
		while ((*map)[i][j])
		{
			if ((*map)[i][j] == ' ' || (*map)[i][j] == '\t')
				((*map)[i][j] = c);
			j++;
		}
		i++;
	}
}
